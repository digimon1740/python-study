# 튜플
for i in (1, 2, 3):
    print(i)

# 리스트
for s in ['뇌를', '자극하는', '파이썬']:
    print(s)

# 문자열
for s in '뇌를 자극하는 파이썬':
    print(s)

# range()
for i in range(0, 5, 1):
    print(i)

for i in range(0, 10, 2):
    print(i)

for i in range(0, 5):
    print(i)

for i in range(5):
    print(i)

dic = {'애플': 'www.apple.com', '파이썬': 'www.python.org', '마이크로소프트': 'www.microsoft.com'}

for key, value in dic.items():
    print('{0} : {1}'.format(key, value))





